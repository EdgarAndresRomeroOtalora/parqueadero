    public void getObjetos() {
        if (util.desSerializar(NOM_ARCHIVO_Lista) != null) {
            objetosLista = (ArrayList<Object>) util.desSerializar(NOM_ARCHIVO_Lista);
            listasencilla = (Lista) objetosLista.get(0);
            objetosLista.clear();

        }
    }

    public void llenarCbxNumParqueadero() {

        for (int i = 1; i < 21; i++) {
            int a = 0;
            for (int h = 0; h < listasencilla.vehiculosParqueados(nodo1).length; h++) {
                if (listasencilla.vehiculosParqueados(nodo1)[h] == i) {
                    a = 1;
                }
            }
            if (a == 0) {
                this.cbxNumParqueadero.addItem(i);
            }

        }

    }

    
