    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        Controlador controlador = new Controlador();
        int numParqueadero = cbxNumParqueadero.getSelectedItem().hashCode();

        if (listasencilla.hayPlacaIgual(nodo1)) {
            JOptionPane.showMessageDialog(null, "Esta vehiculo ya esta parqueado");
        } else {
            Vehiculo vehiculo = controlador.guardarObjeto(txtPlaca.getText(), numParqueadero);
            Nodo nodo1 = new Nodo(vehiculo);

            listasencilla.agregarAlFinal(nodo1);
            objetosLista.add(listasencilla);
            util.serializar(NOM_ARCHIVO_Lista, objetosLista);

            Vparqueadero parqueadero = new Vparqueadero();
            parqueadero.setVisible(true);
            this.setVisible(false);
        }


    }//GEN-LAST:event_btnGuardarActionPerformed

