    private void txtAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtAceptarActionPerformed

        try {
            Vehiculo carro = listasencilla.crearObjetoSalida(nodo1);
            Controlador controlador = new Controlador();
            Date fechaSalida = new Date();

            carro.setCostoParqueo(controlador.costoTotal(carro.getFechaIngreso()));
            carro.setFechaSalida(fechaSalida);

            listasencilla.borrarVehiculo(nodo1);
            objetosLista.add(listasencilla);
            util.serializar(NOM_ARCHIVO_Lista, objetosLista);

            objetosVehiculos.add(carro);
            util.serializar(NOM_ARCHIVO_VehiculosParqueados, objetosVehiculos);

            Vparqueadero parqueadero = new Vparqueadero();
            parqueadero.setVisible(true);
            this.setVisible(false);
        } catch (ParseException ex) {
            Logger.getLogger(VSalida.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_txtAceptarActionPerformed

