    public void getObjetos() {
        if (util.desSerializar(NOM_ARCHIVO_Lista) != null) {
            objetosLista = (ArrayList<Object>) util.desSerializar(NOM_ARCHIVO_Lista);
            listasencilla = (Lista) objetosLista.get(0);
            objetosLista.clear();
        }
        if (util.desSerializar(NOM_ARCHIVO_VehiculosParqueados) != null) {
            objetosVehiculos = (ArrayList<Object>) util.desSerializar(NOM_ARCHIVO_VehiculosParqueados);
        }
    }

    public void llenarCbxConPlacas() {
        for (int h = 0; h < listasencilla.placasVehiculosParqueados(nodo1).length; h++) {
            this.cbxPlacas.addItem(listasencilla.placasVehiculosParqueados(nodo1)[h]);
        }
    }

